import java.util.*;
public class duplicateRemove {
    public static void remove(String str, int index){
        if(index==str.length()){
            return;
        }
    }
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter a string:");
        String str = sc.nextLine();

        remove(str,0);

    }
}
